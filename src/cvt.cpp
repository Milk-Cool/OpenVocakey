String cvt_syl(String& syl) {
    if(!prolong_map.contains(syl)) return syl;
    String o = syl;
    for(int i = 0; i < g_stretch; i++)
        o += prolong_map[syl];
    return o;
}
void set_stretch(int stretch) {
    g_stretch = stretch;
}
int get_stretch() {
    return g_stretch;
}